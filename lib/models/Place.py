


class Place:
    def __init__(self, UserToken = None, LatLon = None, \
        Name = None, Type = None, IsPreferredPlace = None, \
            IsFavourite = None, PreferredTimeStart = None, \
                PreferredTimeEnd = None):
        self.UserToken = UserToken
        self.LatLon = LatLon
        self.Name = Name
        self.Type = Type
        self.IsPreferredPlace = IsPreferredPlace
        self.IsFavourite = IsFavourite
        self.PreferredTimeStart = PreferredTimeStart
        self.PreferredTimeEnd = PreferredTimeEnd
        
